y=0
y=int(input('输入当月利润（万）：'))
x=int(y)
if x > 0 and x <= 10 :
    y = x * 0.1
elif x > 10 and x <= 20:
    y = 10 * 0.1 + (x - 10) * 0.075
elif x > 20 and x <= 40:
    y = 10 * 0.1 + 10 * 0.075 + (x - 20) * 0.05
elif x > 40 and x <= 60:
    y = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (x - 40) * 0.03
elif x > 60 and x <= 100:
    y = 20 * 0.175 + 20 * 0.05 + 20 * 0.03 + (x - 60) * 0.015
elif x > 100:
    y = 20 * 0.175 + 40 * 0.08 + 40 * 0.015 + (x - 100) * 0.01
print('应该提取的奖金是'+str(y)+'万')