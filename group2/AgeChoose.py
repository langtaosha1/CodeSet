#  * 读入一个整数，表示一个人的年龄。
#  * 如果小于6岁,则输出“儿童”,
#  * 6岁到13岁，输出“少儿”;
#  * 14岁到18岁，输出“青少年”;
#  * 18岁到35岁，输出“青年”;
#  * 35岁到50岁，输出“中年”;
#  * 50 岁以上输出“中老年”。
# ,,,
m=input('请输入年龄：')
if int(m)<6:
    print('儿童')
if 6<=int(m)<13 :
    print('少儿')
if 13<=int(m)<18:
    print('青少年')
if 18<=int(m)<35:
    print('青年')
if 35<=int(m)<50:
    print('中年')
if int(m)>50:
    print('中年')